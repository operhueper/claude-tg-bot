export function pickRandomPhrase(exclude?: string): string {
  const candidates =
    exclude && IDLE_PHRASES.length > 1
      ? IDLE_PHRASES.filter((p) => p !== exclude)
      : IDLE_PHRASES;
  return candidates[Math.floor(Math.random() * candidates.length)]!;
}
